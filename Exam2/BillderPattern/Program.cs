using System;

namespace BillderPattern
{
    class Program
    {
        static void Main(string[] args)
        {
            var pizza = new PizzaBuilder().SetFlavour("Venilla")
                .SetColour()
                .SetSize()
                .Build();

            Console.WriteLine($"Flavour: {pizza.Flavour}");
            Console.WriteLine($"Colour: {pizza.HasColour}");
            Console.WriteLine($"Size: {pizza.HasSize}");
            
        }
    }
}
